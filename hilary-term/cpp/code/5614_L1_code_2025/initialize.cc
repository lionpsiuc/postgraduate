int main(void)
{
    int a1 {1}; 	// Initialises a1 to value 1
    int a2 = {2}; 	// Initialises a2 to value 2
    int a3 = 3; 	// Initialises a3 to value 3
    int a4(4); 		// Initialises a4 to value 4
    return 0;
}

